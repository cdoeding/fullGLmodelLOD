function [tau,w] = get_gauss_nodes(q,a,b)

switch q
    case 1
        tau0 = zeros(1,1);
        w0 = zeros(1,1);
        tau0(1) = 0;
        w0(1) = 2;
        
    case 2
        tau0 = [-sqrt(1/3), sqrt(1/3)];
        w0 = [1, 1];  
    case 3
        tau0 = [-sqrt(3/5), 0,  sqrt(3/5)];
        w0 = [5/9, 8/9, 5/9];  
        
    case 4
        tau0 = [-sqrt((3/7) + (2/7)*sqrt(6/5)), ...
                -sqrt((3/7) - (2/7)*sqrt(6/5)), ...
                sqrt((3/7) - (2/7)*sqrt(6/5)), ...
                sqrt((3/7) + (2/7)*sqrt(6/5)) ];
        w0 = [(18 - sqrt(30))/36, ... 
              (18 + sqrt(30))/36, ... 
              (18 + sqrt(30))/36, ...
              (18 - sqrt(30))/36 ];  
        
    case 5
        tau0 = [-(1/3)*sqrt(5 + 2*sqrt(10/7)), ...
                -(1/3)*sqrt(5 - 2*sqrt(10/7)), ...
                0, ...
                (1/3)*sqrt(5 - 2*sqrt(10/7)), ...
                (1/3)*sqrt(5 + 2*sqrt(10/7)) ];
        w0 = [(322 - 13*sqrt(70))/900, ...
              (322 + 13*sqrt(70))/900, ...
              128/225, ...
              (322 + 13*sqrt(70))/900, ...
              (322 - 13*sqrt(70))/900 ];  
        
    case 6
        tau0 = [ -0.932469514203152, ...
                 -0.661209386466265, ...
                 -0.238619186083197, ...
                  0.238619186083197, ...
                  0.661209386466265, ...
                  0.932469514203152 ];
        w0 = [ 0.171324492379170, ...
               0.360761573048139, ...
               0.467913934572691, ...
               0.467913934572691, ...
               0.360761573048139, ...
               0.171324492379170 ];  
        
    otherwise
        disp('order not supported.')
end

tau = tau0.*(b-a)/2 + (a+b)/2;
w = w0.*(b-a)/2;



